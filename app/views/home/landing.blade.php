    <script>
      var app = angular.module('App', [])
      app.controller('LandingCtrl', ['$scope', '$http', function ($scope, $http) {
        window.s = $scope
        $scope.otpObj = {}
        $scope.login = login
        $scope.register = register
        $scope.otpVerification = otpVerification
        $scope.forgotPasswordModal = forgotPasswordModal
        $scope.forgotPassword = forgotPassword

        function login (obj) {
          if ($scope.loginFrm.$invalid) {
            toastr.error('Fill all the details.')
            return
          }
          $http.post('/login', obj)
            .then(function (data) {
              toastr.success('Successfully logged in')
              setTimeout(function () {
                window.location.href = '/'
              }, 2000)
            }, function (err) {
              if (! err.data.code) {
                toastr.error('Invalid login credentials')
                return
              }
              if (err.data.code == 2) {
                toastr.error('Email is not verified. Please verify you\'re email and try again.')
              }
              if (err.data.code == 3) {
                toastr.error('Mobile no. is not verified. Please verify you\'re mobile no. and try again.')
                $scope.otpObj.email = err.data.email
                $('#sessionModal').modal('hide')
                $('#OTPModal').modal('show')
              }
              if (err.data.code == 4) {
                toastr.error("You're account is inactive. Contact the site administrator.")
              }
            })
        }
        function register (obj) {
          if ($scope.registerFrm.$invalid) {
            toastr.error('Fill all the details.')
            return
          }
          if (obj.password !== obj.password_conf) {
            toastr.error('Password and password confirmation do not match.')
            return
          }
          $http.post('/register', obj)
            .then(function (data) {
              toastr.success('Registration is successfull done.')
              toastr.success('An mail has been sent to you with the email-verification link. Please verify you\'re email before you try to login.')
              $('#sessionModal').modal('hide')
              $('#OTPModal').modal('show')
              $scope.otpObj.email = data.data.email
            }, fail)
        }
        function otpVerification (obj) {
          if ($scope.OTPFrm.$invalid) {
            toastr.error('Fill all the details.')
            return
          }
          $http.post('/verification/phone/'+obj.otp, obj)
            .then(function (data) {
              toastr.success('Mobile no. is verified. Please login to continue.')
              $('#OTPModal').modal('hide')
              $('#sessionModal').modal('show')
            }, function (err) {
              if (err.status == 400) {
                toastr.error('Please send email')
                return
              }
              toastr.error('OTP entered is invalid')
            })
        }
        function forgotPasswordModal () {
          $('#sessionModal').modal('hide')
          $('#forgotPasswordModal').modal('show')
        }
        function forgotPassword (obj) {
          if ($scope.forgotPasswordFrm.$invalid) {
            toastr.error('Fill all the details.')
            return
          }
          $http.post('/password/actions/reset-token', obj)
            .then(function (data) {
              toastr.success('A password reset email has been sent.')
              $('#forgotPasswordModal').modal('hide')
            }, function (err) {
              if (err.status === 400) {
                toastr.error('Please end your email.')
                return
              }
              toastr.error('User with that email not found')
            })
        }

        function fail (err) {
          toastr.error('Something wrong occured')
        }
      }])
    </script>
